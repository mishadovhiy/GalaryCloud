import SwiftUI

struct TrashShape: Shape {
    func path(in rect: CGRect) -> Path {
        var path = Path()
        let width = rect.size.width
        let height = rect.size.height
        path.move(to: CGPoint(x: 0.88462*width, y: 0.25*height))
        path.addLine(to: CGPoint(x: 0.11538*width, y: 0.25*height))
        path.addCurve(to: CGPoint(x: 0.07692*width, y: 0.21875*height), control1: CGPoint(x: 0.09415*width, y: 0.25*height), control2: CGPoint(x: 0.07692*width, y: 0.23603*height))
        path.addCurve(to: CGPoint(x: 0.11538*width, y: 0.1875*height), control1: CGPoint(x: 0.07692*width, y: 0.2015*height), control2: CGPoint(x: 0.09415*width, y: 0.1875*height))
        path.addLine(to: CGPoint(x: 0.88462*width, y: 0.1875*height))
        path.addCurve(to: CGPoint(x: 0.92308*width, y: 0.21875*height), control1: CGPoint(x: 0.90585*width, y: 0.1875*height), control2: CGPoint(x: 0.92308*width, y: 0.2015*height))
        path.addCurve(to: CGPoint(x: 0.88462*width, y: 0.25*height), control1: CGPoint(x: 0.92308*width, y: 0.23603*height), control2: CGPoint(x: 0.90585*width, y: 0.25*height))
        path.addLine(to: CGPoint(x: 0.88462*width, y: 0.25*height))
        path.closeSubpath()
        path.move(to: CGPoint(x: 0.84615*width, y: 0.875*height))
        path.addCurve(to: CGPoint(x: 0.76923*width, y: 0.9375*height), control1: CGPoint(x: 0.84615*width, y: 0.9095*height), control2: CGPoint(x: 0.81169*width, y: 0.9375*height))
        path.addLine(to: CGPoint(x: 0.23077*width, y: 0.9375*height))
        path.addCurve(to: CGPoint(x: 0.15385*width, y: 0.875*height), control1: CGPoint(x: 0.18831*width, y: 0.9375*height), control2: CGPoint(x: 0.15385*width, y: 0.9095*height))
        path.addLine(to: CGPoint(x: 0.15385*width, y: 0.3125*height))
        path.addLine(to: CGPoint(x: 0.84615*width, y: 0.3125*height))
        path.addLine(to: CGPoint(x: 0.84615*width, y: 0.875*height))
        path.addLine(to: CGPoint(x: 0.84615*width, y: 0.875*height))
        path.closeSubpath()
        path.move(to: CGPoint(x: 0.38462*width, y: 0.09375*height))
        path.addCurve(to: CGPoint(x: 0.42308*width, y: 0.0625*height), control1: CGPoint(x: 0.38462*width, y: 0.07647*height), control2: CGPoint(x: 0.40185*width, y: 0.0625*height))
        path.addLine(to: CGPoint(x: 0.57692*width, y: 0.0625*height))
        path.addCurve(to: CGPoint(x: 0.61538*width, y: 0.09375*height), control1: CGPoint(x: 0.59815*width, y: 0.0625*height), control2: CGPoint(x: 0.61538*width, y: 0.07647*height))
        path.addLine(to: CGPoint(x: 0.61538*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.38462*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.38462*width, y: 0.09375*height))
        path.addLine(to: CGPoint(x: 0.38462*width, y: 0.09375*height))
        path.closeSubpath()
        path.move(to: CGPoint(x: 0.92308*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.69231*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.69231*width, y: 0.0625*height))
        path.addCurve(to: CGPoint(x: 0.61538*width, y: 0), control1: CGPoint(x: 0.69231*width, y: 0.028*height), control2: CGPoint(x: 0.65785*width, y: 0))
        path.addLine(to: CGPoint(x: 0.38462*width, y: 0))
        path.addCurve(to: CGPoint(x: 0.30769*width, y: 0.0625*height), control1: CGPoint(x: 0.34215*width, y: 0), control2: CGPoint(x: 0.30769*width, y: 0.028*height))
        path.addLine(to: CGPoint(x: 0.30769*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.07692*width, y: 0.125*height))
        path.addCurve(to: CGPoint(x: 0, y: 0.1875*height), control1: CGPoint(x: 0.03446*width, y: 0.125*height), control2: CGPoint(x: 0, y: 0.153*height))
        path.addLine(to: CGPoint(x: 0, y: 0.25*height))
        path.addCurve(to: CGPoint(x: 0.07692*width, y: 0.3125*height), control1: CGPoint(x: 0, y: 0.2845*height), control2: CGPoint(x: 0.03446*width, y: 0.3125*height))
        path.addLine(to: CGPoint(x: 0.07692*width, y: 0.875*height))
        path.addCurve(to: CGPoint(x: 0.23077*width, y: height), control1: CGPoint(x: 0.07692*width, y: 0.94403*height), control2: CGPoint(x: 0.14581*width, y: height))
        path.addLine(to: CGPoint(x: 0.76923*width, y: height))
        path.addCurve(to: CGPoint(x: 0.92308*width, y: 0.875*height), control1: CGPoint(x: 0.85419*width, y: height), control2: CGPoint(x: 0.92308*width, y: 0.94403*height))
        path.addLine(to: CGPoint(x: 0.92308*width, y: 0.3125*height))
        path.addCurve(to: CGPoint(x: width, y: 0.25*height), control1: CGPoint(x: 0.96554*width, y: 0.3125*height), control2: CGPoint(x: width, y: 0.2845*height))
        path.addLine(to: CGPoint(x: width, y: 0.1875*height))
        path.addCurve(to: CGPoint(x: 0.92308*width, y: 0.125*height), control1: CGPoint(x: width, y: 0.153*height), control2: CGPoint(x: 0.96554*width, y: 0.125*height))
        path.addLine(to: CGPoint(x: 0.92308*width, y: 0.125*height))
        path.closeSubpath()
        return path
    }
}

